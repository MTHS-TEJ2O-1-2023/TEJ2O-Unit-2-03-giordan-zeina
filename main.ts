// Clean up
basic.clearScreen()
basic.showIcon(IconNames.Happy)
basic.pause(1000)

// Area
basic.clearScreen()
basic.showString('The area of a rectangle with the dimensions 5 cm and 3 cm is' + (5 * 3) + 'cm^2')
basic.showIcon(IconNames.Happy)

// Perimeter
basic.clearScreen()
basic.showString('The perimeter for a rectangle with the dimensions 5 cm and 3 cm is' + (2 * (5 + 3)) + 'cm')
basic.showIcon(IconNames.Happy)
